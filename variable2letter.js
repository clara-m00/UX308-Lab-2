// This program *converts* a **variable** to a **letter grade**

let grade = 87

if (grade < 50) {
    console.log ("F");
}

else if (grade >= 50 && grade <= 59 ){
    console.log ("D");
}

else if (grade >= 60 && grade <= 69){
    console.log ("C");
}

else if (grade >= 70 && grade <= 79){
    console.log ("B");
}

else if (grade >= 80 && grade <= 89){
    console.log ("A");
}

else if (grade >= 90 && grade <= 99){
    console.log ("A+");
}

else if (grade >= 100){
    console.log ("Perfect!");
}
  
        